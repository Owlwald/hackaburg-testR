import Foundation
import UIKit

extension UIColor {

    static func navBarColor() -> UIColor {
        return UIColor(red: 0.145, green: 0.145, blue: 0.996, alpha: 1)
    }
    
    static func lightBlueBackgroundColor() -> UIColor {
//        UIColor colorWithRed:0.831 green:0.89 blue:0.996 alpha:1] /*#d4e3fe*/
        return UIColor(red: 0.831, green: 0.89, blue: 0.996, alpha: 1)
    }
}